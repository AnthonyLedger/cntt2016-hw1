class	PyramidSequences{
	public:
		long long distinctPairs(int N, int M){
			N--;
			M--;
			
			int	g= __gcd(N, M);
			int	X= N/g, Y= M/g;
			return	1ll*(X+1)*(Y+1)/2 + 1ll*(g-1)*X*Y;	//找规律输出答案
		}
};