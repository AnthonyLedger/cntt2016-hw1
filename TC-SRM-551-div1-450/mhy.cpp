class ColorfulWolves {
    public:
        int dist[105][105];
        int getmin(vector<string> colormap) {
            int n=colormap.size();
            for (int i=0;i<n;i++) for (int j=0;j<n;j++){
                int c=0;
                for (int k=0;k<j;k++) if (colormap[i][k]=='Y') c++;
                if (colormap[i][j]=='N') c=0x3f3f3f3f;
                dist[i][j]=c;
            }
            for (int k=0;k<n;k++) for (int i=0;i<n;i++) for (int j=0;j<n;j++) dist[i][j]=min(dist[i][j],dist[i][k]+dist[k][j]);
            return dist[0][n-1]!=0x3f3f3f3f?dist[0][n-1]:-1;
        }
};

