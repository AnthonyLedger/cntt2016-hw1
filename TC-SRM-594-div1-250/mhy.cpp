class AstronomicalRecords {
    public:
        long long a[105],b[105];
        int dp[105][105];
        int minimalPlanets(vector<int> A, vector<int> B) {
            int n=A.size(),m=B.size(),ans=n+m;
            for (int x=0;x<n;x++) for (int y=0;y<m;y++){
                for (int i=0;i<n;i++) a[i+1]=1ll*A[i]*B[y];
                for (int i=0;i<m;i++) b[i+1]=1ll*B[i]*A[x];
                memset(dp,0,sizeof(dp));
                for (int i=1;i<=n;i++) for (int j=1;j<=m;j++) dp[i][j]=max(max(dp[i-1][j],dp[i][j-1]),a[i]==b[j]?dp[i-1][j-1]+1:0);
                ans=min(ans,n+m-dp[n][m]);
            }
            return ans;
        }
};

